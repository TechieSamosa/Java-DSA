import java.util.*;

public class InSum {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the 1st Number");
        int a = sc.nextInt();
        System.out.println("Enter the 2nd Number");
        int b = sc.nextInt();
        int c = (a+b);
        System.out.println("The Sum of both numbers is :"+c);
    }
}
